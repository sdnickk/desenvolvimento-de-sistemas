package com.mycompany.sencostan;

public class SenCosTan {

    public static void main(String[] args) {
        double angulo = Math.toRadians(45);
        
        System.out.println("seno de 45 graus: " + Math.sin(angulo));
        System.out.println("cosseno de 45 graus: " + Math.cos(angulo));
        System.out.println("Tangente de 45 graus: " + Math.tan(angulo));
    }
}
